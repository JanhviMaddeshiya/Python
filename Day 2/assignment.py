x = 5
print("Initial value of x:", x)

x += 2   # x = x + 2
print("After += operation, x:", x)

x -= 3   # x = x - 3
print("After -= operation, x:", x)

x *= 2   # x = x * 2
print("After *= operation, x:", x)

x /= 2   # x = x / 2
print("After /= operation, x:", x)

x %= 3   # x = x % 3
print("After %= operation, x:", x)